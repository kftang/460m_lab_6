module tb_matrix_multiplier;

    reg clk;
    reg [7:0] a [2:0][2:0];
    reg [7:0] b [2:0][2:0];
    wire [7:0] ab [2:0][2:0];
    wire done;
    
    matrix_multiplier matrix_multiplier(
        .clk(clk),
        .a00(a[0][0]),
        .a01(a[0][1]),
        .a02(a[0][2]),
        .a10(a[1][0]),
        .a11(a[1][1]),
        .a12(a[1][2]),
        .a20(a[2][0]),
        .a21(a[2][1]),
        .a22(a[2][2]),
        .b00(b[0][0]),
        .b01(b[0][1]),
        .b02(b[0][2]),
        .b10(b[1][0]),
        .b11(b[1][1]),
        .b12(b[1][2]),
        .b20(b[2][0]),
        .b21(b[2][1]),
        .b22(b[2][2]),
        .ab00(ab[0][0]),
        .ab01(ab[0][1]),
        .ab02(ab[0][2]),
        .ab10(ab[1][0]),
        .ab11(ab[1][1]),
        .ab12(ab[1][2]),
        .ab20(ab[2][0]),
        .ab21(ab[2][1]),
        .ab22(ab[2][2]),
        .done(done)
    );
    
    initial begin
    // A
    // 1.5 2.0 -1.0     = 1.1000, 10.000, -1.0000
    // 1.0 0.5 -0.75    = 1.0000, 0.100, -0.1100
    // -1.25 0 1.25     = -1.0100, 0, 1.0100
    a[0][0] = 8'b00111000;
    a[0][1] = 8'b01000000;
    a[0][2] = 8'b10110000;
    
    a[1][0] = 8'b00110000;
    a[1][1] = 8'b00100000;
    a[1][2] = 8'b10101000;
    
    a[2][0] = 8'b10110100;
    a[2][1] = 8'b00000000;
    a[2][2] = 8'b00100100;
    
    // B
    // -1.0 0.75 3.0        = -1.0000, 0.11000, 11.000
    // -2.25 3.25 1.625     = -10.010, 11.010, 1.1010
    // 0     -3.5 0.25      = 0      , -11.100, 0.010000
    b[0][0] = 8'b10110000;
    b[0][1] = 8'b00101000;
    b[0][2] = 8'b01001000;
    
    b[1][0] = 8'b11000010;
    b[1][1] = 8'b01001010;
    b[1][2] = 8'b00111010;
    
    b[2][0] = 8'b00000000;
    b[2][1] = 8'b11001100;
    b[2][2] = 8'b00010000;
    
    end
    
endmodule
